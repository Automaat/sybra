// Command gen-events regenerates frontend/src/lib/events.ts from the Go
// event constants in internal/events/events.go. Run via `go generate ./...`
// (the directive lives in internal/events/events.go).
//
// The generator parses the Go source with go/parser — no reflection, no
// build — so it works in a pre-build step and stays friendly to hermetic
// environments. For every string-typed const it emits a TypeScript
// `export const NAME = 'value'`, and for every func that returns a string
// by concatenating a "*Prefix" constant with a parameter it emits a
// matching TS arrow function. This keeps the existing helper shape
// (`agentState(id: string) => ...`) without hand-maintaining a mirror
// file.
package main

import (
	"cmp"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"slices"
	"strconv"
	"strings"
)

// findModuleRoot walks up from the current dir looking for go.mod. The
// generator is invoked from internal/events (via go:generate), so we need
// to climb out to the repo root before writing to frontend/src/lib.
func findModuleRoot(start string) (string, error) {
	dir := start
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found starting from %s", start)
		}
		dir = parent
	}
}

type constDecl struct {
	name  string
	value string
}

type funcDecl struct {
	name      string
	paramName string
	prefix    string // name of the *Prefix const referenced
}

// parseEventsGo extracts every string-typed top-level const and every
// helper function that returns `<PrefixConst> + paramName`. Helpers whose
// shape doesn't match are skipped with a warning so we don't silently
// drop frontend functionality if the Go signature changes.
func parseEventsGo(path string) ([]constDecl, []funcDecl, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, path, nil, parser.ParseComments)
	if err != nil {
		return nil, nil, fmt.Errorf("parse %s: %w", path, err)
	}

	var consts []constDecl
	var funcs []funcDecl

	for _, decl := range f.Decls {
		switch d := decl.(type) {
		case *ast.GenDecl:
			if d.Tok != token.CONST {
				continue
			}
			for _, spec := range d.Specs {
				vs, ok := spec.(*ast.ValueSpec)
				if !ok {
					continue
				}
				for i, name := range vs.Names {
					if i >= len(vs.Values) {
						continue
					}
					lit, ok := vs.Values[i].(*ast.BasicLit)
					if !ok || lit.Kind != token.STRING {
						continue
					}
					unquoted, uErr := strconv.Unquote(lit.Value)
					if uErr != nil {
						continue
					}
					consts = append(consts, constDecl{name: name.Name, value: unquoted})
				}
			}
		case *ast.FuncDecl:
			if d.Recv != nil || d.Type.Params == nil || len(d.Type.Params.List) != 1 {
				continue
			}
			param := d.Type.Params.List[0]
			if len(param.Names) != 1 {
				continue
			}
			paramName := param.Names[0].Name
			if d.Body == nil || len(d.Body.List) != 1 {
				continue
			}
			ret, ok := d.Body.List[0].(*ast.ReturnStmt)
			if !ok || len(ret.Results) != 1 {
				continue
			}
			bin, ok := ret.Results[0].(*ast.BinaryExpr)
			if !ok || bin.Op != token.ADD {
				continue
			}
			ident, ok := bin.X.(*ast.Ident)
			if !ok {
				continue
			}
			rhs, ok := bin.Y.(*ast.Ident)
			if !ok || rhs.Name != paramName {
				continue
			}
			funcs = append(funcs, funcDecl{
				name:      d.Name.Name,
				paramName: paramName,
				prefix:    ident.Name,
			})
		}
	}
	return consts, funcs, nil
}

// renderTS produces the TypeScript content. Const declarations are emitted
// in the source order they were parsed; funcs follow. A deterministic
// header makes go-generate diffs stable across runs.
func renderTS(consts []constDecl, funcs []funcDecl) string {
	var b strings.Builder
	b.WriteString("// Code generated by cmd/gen-events from internal/events/events.go. DO NOT EDIT.\n")
	b.WriteString("//\n")
	b.WriteString("// Run `go generate ./internal/events/...` to regenerate after changing the Go\n")
	b.WriteString("// source. The drift guard test TestEventsTSFile_InSyncWithGoSource catches\n")
	b.WriteString("// any manual edits in CI.\n\n")

	for _, c := range consts {
		fmt.Fprintf(&b, "export const %s = %s\n", c.name, strconv.Quote(c.value))
	}
	b.WriteString("\n")

	// Emit helper fns with a lowercased first character (matches the
	// existing `agentState`/`agentOutput` convention in the hand-maintained
	// file this generator replaces).
	sortedFuncs := make([]funcDecl, len(funcs))
	copy(sortedFuncs, funcs)
	slices.SortStableFunc(sortedFuncs, func(a, b funcDecl) int {
		return cmp.Compare(a.name, b.name)
	})
	for _, fn := range sortedFuncs {
		lower := strings.ToLower(fn.name[:1]) + fn.name[1:]
		fmt.Fprintf(&b, "export const %s = (%s: string) => `${%s}${%s}`\n",
			lower, fn.paramName, fn.prefix, fn.paramName)
	}
	return b.String()
}

func main() {
	cwd, err := os.Getwd()
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen-events:", err)
		os.Exit(1)
	}
	root, err := findModuleRoot(cwd)
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen-events:", err)
		os.Exit(1)
	}

	source := filepath.Join(root, "internal", "events", "events.go")
	dest := filepath.Join(root, "frontend", "src", "lib", "events.ts")

	consts, funcs, err := parseEventsGo(source)
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen-events:", err)
		os.Exit(1)
	}
	content := renderTS(consts, funcs)
	if err := os.WriteFile(dest, []byte(content), 0o644); err != nil {
		fmt.Fprintln(os.Stderr, "gen-events: write:", err)
		os.Exit(1)
	}
	fmt.Printf("gen-events: wrote %s (%d consts, %d funcs)\n", dest, len(consts), len(funcs))
}
